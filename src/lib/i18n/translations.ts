/** Preserve key structure from EN but allow any string value (so ES strings are valid). */
export type Translations = {
  readonly [K in keyof typeof translations.en]: {
    readonly [J in keyof (typeof translations.en)[K]]: string;
  };
};
export type TranslationKey = keyof Translations;

/** Get a flat translation accessor for the given language. */
export function getT(lang: Lang): Translations {
  return (translations[lang] ?? translations.en) as Translations;
}
